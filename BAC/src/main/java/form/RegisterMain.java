    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        
        String plateNumber = platefield.getText();
        String brand = brandfield.getText();
        String model = modelfield.getText();
        String description = descfield.getText();
        double price;
        String transmission;

        try {
            price = Double.parseDouble(pricefield.getText());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid price.", "Invalid Input", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
       
        
        boolean isAutomatic = autoYes.isSelected();
        ICar carFactory = Factory.getFactory(isAutomatic);
        
        if (isAutomatic){
            transmission = "Automatic";
        } else {
            transmission = "Manual";
        }
        Car newCar = carFactory.createCar(plateNumber, brand, model, description, transmission, price);
        
        //add and check if added sa fleet inventory
        boolean added = fi.AddCar(newCar);

        if (added){
            //update text file dito
            fi.writeFile();

            //Update the table model
            ArrayList<CarRep> rep = fi.generateReport(ro);
            updateProperties(rep);
            
            //Display message na success
            javax.swing.JOptionPane.showMessageDialog(this, "Car added successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        
        //Clear input fields
        platefield.setText("");
        brandfield.setText("");
        modelfield.setText("");
        descfield.setText("");
        pricefield.setText("");

    }                                      

    private void rembtnActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        
        int selectIndex = tbl.getSelectedRow();

        // Check if a row is selected, if not, ulit
        if (selectIndex == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a car to delete.", "No Selection", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirm if delete
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the selected car?", "Confirm Removal", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        //plate number 1st colum = 0
        String plateNumber = tbl.getValueAt(selectIndex, 0).toString();

        //assign delete car to car w/ plate number to be deleted 
        Car delCar = fi.getCarByPlateNum(plateNumber);
        
        boolean removed = fi.RemoveCar(delCar);

        if (removed) {
            fi.writeFile();

            ArrayList<CarRep> rep = fi.generateReport(ro);
            updateProperties(rep);

            javax.swing.JOptionPane.showMessageDialog(this, "Car deleted successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            
            javax.swing.JOptionPane.showMessageDialog(this, "Car is not deleted.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
          
            ArrayList<CarRep> rep = fi.generateReport(ro);
            updateProperties(rep);
            
    }                                      

